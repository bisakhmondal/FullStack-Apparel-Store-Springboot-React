    private final UserInfoService userInfoService;
    private final EmailService emailService;

    public String register(UserInfo userInfo) {
        if(!emailValidator.test(userInfo.getEmail()) ){
            throw new IllegalArgumentException("email is not valid");
        }
        String token = userInfoService.register(userInfo);

        String link = ConfigInfo.SELF_ROUTE + "/api/v1/registration/confirm?token="+token;
        emailService.send(userInfo.getEmail(), buildEmail(userInfo.getFirstName(), link));
        return "An email has been sent, please verify within 15 minutes";
    }

    public String confirmToken(String token) {
        return userInfoService.confirm(token);
    }

